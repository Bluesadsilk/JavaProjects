package EntornoDeDesarrollo;

public class edprueba {

    public static void main(String[] args) {

        int cuenta = 1;
        int N = 10;

        System.out.println("Inicio de la cuenta");
        while (cuenta <= N) {
            System.out.println("Cuenta: " + cuenta);
            cuenta++;

        }
        System.out.println("Fin de la cuenta");
    }

}
